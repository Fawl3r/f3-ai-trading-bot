    def verify_policy_exists(self) -> bool:
        """Verify the policy exists in the bandit database"""
        
        db_path = self.models_dir / "policy_bandit.db"
        if not db_path.exists():
            logger.error(f"âŒ Policy database not found: {db_path}")
            return False
        
        try:
            with sqlite3.connect(str(db_path)) as conn:
                cursor = conn.cursor()
                
                cursor.execute('''
                    SELECT p.id, p.name, ba.traffic_allocation, ba.total_trades
                    FROM policies p
                    JOIN bandit_arms ba ON p.id = ba.policy_id
                    WHERE p.name LIKE ?
                    ORDER BY p.created_at DESC LIMIT 1
                ''', (f'%{self.policy_sha[-8:]}%',))
                
                policy = cursor.fetchone()
                
                if policy:
                    policy_id, name, traffic, trades = policy
                    logger.info(f"âœ… Policy found: {name}")
                    logger.info(f"ğŸ“Š Current traffic: {traffic:.1%}, Trades: {trades}")
                    return True
                else:
                    logger.error(f"âŒ Policy not found: {self.policy_sha}")
                    return False
                    
        except Exception as e:
            logger.error(f"âŒ Database error: {e}")
            return False
    
